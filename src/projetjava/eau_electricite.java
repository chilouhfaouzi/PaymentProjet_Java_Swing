    private void jTextField_nfactureFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_nfactureFocusLost
        /**
        * if the text field is equal to numero_facture or empty
        * we will set the "numero_facture" text in the field
        * on focus lost event
        */
        if (jTextField_nfacture.getText().trim().equals("") || jTextField_nfacture.getText().trim().toLowerCase().equals("numero_facture") ) {
            jTextField_nfacture.setText("numero_facture");
            jTextField_nfacture.setForeground(new Color(153,153,153));

        }

    }//GEN-LAST:event_jTextField_nfactureFocusLost

